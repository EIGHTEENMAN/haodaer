function findBookEnd(content, bookId) {
  const regex = new RegExp("id: '" + bookId + "-s(\\d+)'", 'g');
  let match;
  let lastSectionNum = 0;
  let lastIndex = 0;
  while ((match = regex.exec(content)) !== null) {
    const num = parseInt(match[1]);
    if (num > lastSectionNum) { lastSectionNum = num; lastIndex = match.index; }
  }
  if (lastSectionNum === 0) return null;
  // Find the },    ] }, after the last section
  const closePos = content.indexOf('},    ] },', lastIndex);
  if (closePos === -1) return null;
  return { last: lastSectionNum, insertPos: closePos + '}'.length };
}

