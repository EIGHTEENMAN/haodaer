def append_sections(text, bid, new_sections):
    """找到书籍的sections数组，在最后追加新内容"""
    book_m = re.search(r"id: '" + bid + r"', title: '([^']+)'", text)
    if not book_m:
        return text, 0, f"{bid}: not found"

    sec_pos = text.find("sections: [", book_m.start())
    sec_start = sec_pos + len("sections: [")

    # 找sections结束位置
    next_m = re.search(r"\{ id: '(?:jing|zi|shi|yi|meng)-\d+'", text[book_m.start()+1:])
    next_pos = book_m.start() + 1 + next_m.start() if next_m else len(text)
    area = text[sec_start:next_pos]

    # 找到最后一个 }, 的位置（section的结尾）
    close_pos = area.rfind("    ] },")
    if close_pos < 0:
        return text, 0, f"{bid}: close not found"

    # 在最后一个section之后，sections数组关闭之前插入
    # 找到最后一个 section 的 }}, 之后
    last_section_close = area.rfind("      },")
    if last_section_close < 0:
        return text, 0, f"{bid}: no section found"

    insert_pos = sec_start + last_section_close + len("      },")
    text = text[:insert_pos] + '\n' + new_sections + text[insert_pos:]
    cnt = len(re.findall(r"id: '" + bid + r"-s\d+'", text))
    return text, cnt, f"✅ {bid}: {cnt}节"

